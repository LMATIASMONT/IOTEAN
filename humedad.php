            <h2>Sensor de humedad de la tierra	</h2>
            <p>¡Hola a todos! Hoy les traigo una reseña sobre el Arduino Uno y cómo usarlo con el Sensor de Humedad de la Tierra. Olvídate de los tecnicismos, vamos directo al grano.<br><br>

			<strong>Materiales que necesitas:</strong><br><br>

 - Arduino Uno<br>
 - Sensor de Humedad de la Tierra<br>
 - Cables jumper macho-hembra<br>
 - Una computadora con puerto USB<br>
 - Paso a paso para armarlo:<br><br>

Conecta el Sensor de Humedad de la Tierra al Arduino Uno usando los cables jumper. Asegúrate de seguir las instrucciones del fabricante para la conexión correcta.<br><br>

Conecta el Arduino Uno a tu computadora mediante un cable USB.<br><br>

Descarga e instala el software Arduino IDE en tu computadora desde <a href="https://www.arduino.cc/en/software" target="_blank"> https://www.arduino.cc/en/software.</a><br><br>

Abre el Arduino IDE y selecciona el modelo de tu Arduino Uno en la pestaña "Herramientas > Placa".<br><br>

En la misma pestaña, selecciona el puerto COM al que está conectado tu Arduino Uno.<br><br>

Ahora, ve a "Archivo > Ejemplos > Sensores > DHT" y abre el ejemplo de "DHTtester". Este es un código de ejemplo para probar el Sensor de Humedad de la Tierra.<br><br>

Verás el código en la ventana del Arduino IDE. Compila el código haciendo clic en el icono de verificación.<br><br>

Si la compilación es exitosa, carga el código en tu Arduino Uno haciendo clic en el icono de flecha derecha.<br><br>

Abre la consola serie en el Arduino IDE para ver la lectura de humedad del sensor.<br><br>

¡Listo! Ahora puedes ver la lectura de humedad de la tierra en la consola serie. Este es solo el comienzo, ya que puedes personalizar y expandir tus proyectos a partir de aquí.<br><br>

